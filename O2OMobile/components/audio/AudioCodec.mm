#import "AudioCodec.h"
#import "AudioConfig.h"

#import "amr.h"
#import "caf.h"
#import "wav.h"

@implementation AudioCodec

+ (NSData *)CAF2AMR:(NSData *)cafData
{
	NSData * result = [AMR encode:cafData];
	if ( result )
	{
		fprintf( stderr, "%d/%d, %.2f%%\n", result.length, cafData.length, ((double)result.length / (double)cafData.length) * 100.0 );
	}
	return result;
}

+ (NSData *)AMR2WAV:(NSData *)amrData
{
	return [AMR decode:amrData];
}

@end
